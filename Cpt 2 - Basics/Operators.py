# Arithmetic operators
a=2
b=3
print("The value a+b is ",a+b)
print("The value a-b is ",a-b)
print("The value a*b is ",a*b)
print("The value a/b is ",a/b)
print("The value a**b is ",a**b)
print("The value a//b is ",a//b)    # //= Quotient
print("The value a/'%'/b is ",a%b)  # %= remainder  


# Assignment operators
c=2
print("\n\nThe value of c is ",c)
c+=1
print("\nThe value of c is ",c)

# Comparision  operators  ; they always return values in true or false
a=(2>4)
b=(5>4)
c=(2!=3)
d=(2==3)
print(a)
print(b)
print(c)
print(d)

# Logical Operators ; they always return values in true or false
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is", (bool1 and bool2))  # And is tue when both are true or else false
print("The value of bool1 or bool2 is", (bool1 or bool2))    # or is true when any one of teo is true or else false
print("The value of not bool2 is", (not bool2))              # not changes the value of true to false and vice versa 

